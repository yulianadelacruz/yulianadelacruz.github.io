        <section>
            <h2>Alojamiento</h2>
            <p>El caballo necesita un lugar seguro y cómodo para vivir. Un establo limpio y bien ventilado, con
                suficiente espacio para que el caballo se mueva, es fundamental. El establo debe contar con un buen
                sistema de drenaje y proporcionar protección contra condiciones climáticas extremas, como lluvia, viento
                y calor excesivo.</p>
            <p>Los costos de alojamiento pueden incluir el alquiler de un establo o los materiales para construir uno
                propio. Además, se deben considerar los gastos relacionados con la cama o el forraje utilizado en el
                establo, así como los costos de mantenimiento y limpieza.</p>
            <img src="../img/establo.png" alt="Caballo en el establo">
        </section>

