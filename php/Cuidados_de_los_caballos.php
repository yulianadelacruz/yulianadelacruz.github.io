        <h1>Cuidados del Caballo</h1>
        <p>Los caballos requieren una serie de cuidados específicos para mantener su salud y bienestar. Estos cuidados
            implican ciertos costos, pero son fundamentales para asegurar que el caballo esté en óptimas condiciones. A
            continuación, se presenta un resumen detallado de los principales aspectos a tener en cuenta:</p>

        <section>
            <h2>Alimentación</h2>
            <p>La alimentación adecuada es esencial para un caballo. Se deben proporcionar alimentos de calidad, como
                heno, pasto y concentrados equilibrados. La cantidad y el tipo de alimento pueden variar dependiendo de
                la edad, el nivel de actividad y las necesidades individuales del caballo. Es importante establecer una
                rutina de alimentación regular y proporcionar acceso constante a agua fresca y limpia.</p>
            <p>Los costos de alimentación pueden variar dependiendo de la dieta y la disponibilidad de alimentos en tu
                área. Puedes adquirir los alimentos en tiendas especializadas en productos equinos o directamente en
                granjas y productores locales.</p>
            <img src="../img/caballo_COSTAL.png" alt="Caballo alimentación">
        </section>

